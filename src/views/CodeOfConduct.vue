            <h1 id="gnome-code-of-conduct">GNOME Code of Conduct</h1>

            <p>Thank you for being a part of the GNOME project. We value your participation and want everyone to have an
                enjoyable and fulfilling experience. Accordingly, all participants are expected to follow this Code of
                Conduct, and to show respect, understanding, and consideration to one another. Thank you for helping
                make this a welcoming, friendly community for everyone.</p>

            <h2 id="scope">Scope</h2>

            <p>This Code of Conduct applies to all online GNOME community spaces, including, but not limited to:</p>

            <ul>
                <li>Issue tracking systems - bugzilla.gnome.org</li>
                <li>Documentation and tutorials - developer.gnome.org</li>
                <li>Code repositories - git.gnome.org and gitlab.gnome.org</li>
                <li>Mailing lists - mail.gnome.org</li>
                <li>Wikis - wiki.gnome.org</li>
                <li>Chat and forums - irc.gnome.org, discourse.gnome.org, GNOME Telegram channels, and GNOME groups and
                    channels on Matrix.org (including bridges to GNOME IRC channels)</li>
                <li>Community spaces hosted on gnome.org infrastructure</li>
                <li>Any other channels or groups which exist in order to discuss GNOME project activities</li>
            </ul>

            <p>Communication channels and private conversations that are normally out of scope may be considered in
                scope if a GNOME participant is being stalked or harassed. Social media conversations may be considered
                in-scope if the incident occurred under a GNOME event hashtag, or when an official GNOME account on
                social media is tagged, or within any other discussion about GNOME. The GNOME Foundation reserves the
                right to take actions against behaviors that happen in any context, if they are deemed to be relevant to
                the GNOME project and its participants.</p>

            <p>All participants in GNOME online community spaces are subject to the Code of Conduct. This includes GNOME
                Foundation board members, corporate sponsors, and paid employees. This also includes volunteers,
                maintainers, leaders, contributors, contribution reviewers, issue reporters, GNOME users, and anyone
                participating in discussion in GNOME online spaces.</p>

