            <h3 id="community-guidelines">Community Guidelines</h3>

            <p>Examples of behavior that contributes to creating a positive environment include:</p>

            <ul>
                <li><strong>Be friendly.</strong> Use welcoming and inclusive language.</li>
                <li><strong>Be empathetic.</strong> Be respectful of differing viewpoints and experiences.</li>
                <li><strong>Be respectful.</strong> When we disagree, we do so in a polite and constructive manner.</li>
                <li><strong>Be considerate.</strong> Remember that decisions are often a difficult choice between
                    competing priorities. Focus on what is best for the community. Keep discussions around technology
                    choices constructive and respectful.</li>
                <li><strong>Be patient and generous.</strong> If someone asks for help it is because they need it. When
                    documentation is available that answers the question, politely point them to it. If the question is
                    off-topic, suggest a more appropriate online space to seek help.</li>
                <li><strong>Try to be concise.</strong> Read the discussion before commenting in order to not repeat a
                    point that has been made.</li>
            </ul>

            <h3 id="inappropriate-behavior">Inappropriate Behavior</h3>

            <p>Community members asked to stop any inappropriate behavior are expected to comply immediately.</p>

            <p>We want all participants in the GNOME community have the best possible experience they can. In order to
                be clear what that means, we’ve provided a list of examples of behaviors that are inappropriate for
                GNOME community spaces:</p>

            <ul>
                <li><strong>Deliberate intimidation, stalking, or following.</strong></li>
                <li><strong>Sustained disruption of online discussion, talks, or other events.</strong> Sustained
                    disruption of events, online discussions, or meetings, including talks and presentations, will not
                    be tolerated. This includes ‘Talking over’ or ‘heckling’ event speakers or influencing crowd actions
                    that cause hostility in event sessions. Sustained disruption also includes drinking alcohol to
                    excess or using recreational drugs to excess, or pushing others to do so.</li>
                <li><strong>Harassment of people who don’t drink alcohol.</strong> We do not tolerate derogatory
                    comments about those who abstain from alcohol or other substances. We do not tolerate pushing people
                    to drink, talking about their abstinence or preferences to others, or pressuring them to drink -
                    physically or through jeering.</li>
                <li><strong>Sexist, racist, homophobic, transphobic, ableist language or otherwise exclusionary
                        language.</strong> This includes deliberately referring to someone by a gender that they do not
                    identify with, and/or questioning the legitimacy of an individual’s gender identity. If you’re
                    unsure if a word is derogatory, don’t use it. This also includes repeated subtle and/or indirect
                    discrimination.</li>
                <li><strong>Unwelcome sexual attention or behavior that contributes to a sexualized
                        environment.</strong> This includes sexualized comments, jokes or imagery in interactions,
                    communications or presentation materials, as well as inappropriate touching, groping, or sexual
                    advances. Sponsors should not use sexualized images, activities, or other material. Meetup
                    organizing staff and other volunteer organizers should not use sexualized
                    clothing/uniforms/costumes, or otherwise create a sexualized environment.</li>
                <li><strong>Unwelcome physical contact.</strong> This includes touching a person without permission,
                    including sensitive areas such as their hair, pregnant stomach, mobility device (wheelchair,
                    scooter, etc) or tattoos. This also includes physically blocking or intimidating another person.
                    Physical contact or simulated physical contact (such as emojis like “kiss”) without affirmative
                    consent is not acceptable. This includes sharing or distribution of sexualized images or text.</li>
                <li><strong>Violence or threats of violence.</strong> Violence and threats of violence are not
                    acceptable - online or offline. This includes incitement of violence toward any individual,
                    including encouraging a person to commit self-harm. This also includes posting or threatening to
                    post other people’s personally identifying information (“doxxing”) online.</li>
                <li><strong>Influencing or encouraging inappropriate behavior.</strong> If you influence or encourage
                    another person to violate the Code of Conduct, you may face the same consequences as if you had
                    violated the Code of Conduct.</li>
                <li><strong>Possession of an offensive weapon at a GNOME event.</strong> This includes anything deemed
                    to be a weapon by the event organizers.</li>
            </ul>

            <p>The GNOME community prioritizes marginalized people’s safety over privileged people’s comfort. The
                committee will not act on complaints regarding:</p>

            <ul>
                <li>“Reverse”-isms, including “reverse racism,” “reverse sexism,” and “cisphobia”</li>
                <li>Reasonable communication of boundaries, such as “leave me alone,” “go away,” or “I’m not discussing
                    this with you.”</li>
                <li>Criticizing racist, sexist, cissexist, or otherwise oppressive behavior or assumptions</li>
                <li>Communicating boundaries or criticizing oppressive behavior in a “tone” you don’t find congenial
                </li>
            </ul>

            <p>The examples listed above are not against the Code of Conduct. If you have questions about the above
                statements, please <a
